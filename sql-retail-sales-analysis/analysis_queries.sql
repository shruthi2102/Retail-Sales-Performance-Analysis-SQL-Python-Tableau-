/* -----------------------------------------------------------
Query 1: Total Revenue
Purpose:
Calculate the total revenue generated from all sales.
Revenue is calculated as quantity * price.
----------------------------------------------------------- */

SELECT SUM(quantity * price) AS total_revenue
FROM retail_sales;



/* -----------------------------------------------------------
Query 2: Revenue by Product
Purpose:
Identify which products generate the most revenue.
Useful for product performance analysis.
----------------------------------------------------------- */

SELECT product,
       SUM(quantity * price) AS revenue
FROM retail_sales
GROUP BY product
ORDER BY revenue DESC;



/* -----------------------------------------------------------
Query 3: Revenue by Category
Purpose:
Analyze which product categories generate the highest revenue.
----------------------------------------------------------- */

SELECT category,
       SUM(quantity * price) AS revenue
FROM retail_sales
GROUP BY category
ORDER BY revenue DESC;



/* -----------------------------------------------------------
Query 4: Revenue by City
Purpose:
Identify which cities generate the most sales revenue.
