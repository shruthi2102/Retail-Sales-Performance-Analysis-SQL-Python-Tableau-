/* -----------------------------------------------------------
Query 7: Average Order Value
Purpose:
Calculate the average revenue generated per order.
----------------------------------------------------------- */

SELECT AVG(quantity * price) AS avg_order_value
FROM retail_sales;



/* -----------------------------------------------------------
Query 8: Repeat Customers
Purpose:
Identify customers who placed more than one order.
These customers are important for customer retention analysis.
----------------------------------------------------------- */

SELECT customer_id
FROM retail_sales
GROUP BY customer_id
HAVING COUNT(order_id) > 1;



/* -----------------------------------------------------------
Query 9: Monthly Revenue Trend
Purpose:
Analyze how revenue changes over time (month-wise).
Useful for identifying seasonality and sales trends.
----------------------------------------------------------- */

SELECT DATE_TRUNC('month', order_date) AS month,
       SUM(quantity * price) AS revenue
FROM retail_sales
GROUP BY month
ORDER BY month;



/* -----------------------------------------------------------
