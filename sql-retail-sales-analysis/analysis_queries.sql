    SELECT
        customer_id,
        SUM(quantity * price) AS revenue
    FROM retail_sales
    GROUP BY customer_id
)

SELECT *
FROM customer_revenue
WHERE revenue > 20000
ORDER BY revenue DESC;



/* -----------------------------------------------------------
Query 35: Latest Order per Customer
Purpose:
Use ROW_NUMBER to find the most recent order per customer.
----------------------------------------------------------- */

