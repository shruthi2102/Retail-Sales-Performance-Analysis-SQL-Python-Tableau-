/* -----------------------------------------------------------
Query 38: Month-over-Month Revenue Change
Purpose:
Compare revenue between consecutive months using LAG.
----------------------------------------------------------- */

SELECT
    month,
    revenue,
    revenue - LAG(revenue) OVER(ORDER BY month) AS revenue_change
FROM (
    SELECT
        DATE_TRUNC('month', order_date) AS month,
        SUM(quantity * price) AS revenue
    FROM retail_sales
    GROUP BY month
) t
ORDER BY month;



/* -----------------------------------------------------------
Query 39: Top Product per Category
Purpose:
Find highest revenue product within each category.
Uses PARTITION BY window function.
----------------------------------------------------------- */

SELECT *
FROM (
    SELECT
        p.category,
        p.product_name,
        SUM(r.quantity * r.price) AS revenue,
        RANK() OVER(
            PARTITION BY p.category
            ORDER BY SUM(r.quantity * r.price) DESC
        ) AS category_rank
    FROM retail_sales r
    JOIN products p
    ON r.product_id = p.product_id
    GROUP BY p.category, p.product_name
) ranked
WHERE category_rank = 1;



